const getCategory = (image) => {
  if (image.includes('/feral/')) return 'FERAL';
  if (image.includes('/headbang-dealers/')) return 'Headbang Dealers';
  return 'Night Of Series';
};

const createGalleryItem = (image, featured = false) => {
  const category = getCategory(image);
  return {
    image,
    category,
    featured,
    alt: `Fotografía de ${category} en un evento LICAN`,
  };
};

const featuredSet = new Set(featuredImages);
const categoryOrder = ['FERAL', 'Headbang Dealers', 'Night Of Series'];

const mixByCategory = (images) => {
  const buckets = Object.fromEntries(categoryOrder.map((category) => [category, []]));

  images.forEach((image) => buckets[getCategory(image)].push(image));

  const mixed = [];
  let lastCategory = getCategory(featuredImages[featuredImages.length - 1]);
  let streak = 1;

  while (categoryOrder.some((category) => buckets[category].length)) {
    const available = categoryOrder.filter((category) => (
      buckets[category].length
      && !(category === lastCategory && streak >= 3)
    ));
    const candidates = available.length
      ? available
      : categoryOrder.filter((category) => buckets[category].length);
    const category = candidates.sort((first, second) => (
      buckets[second].length - buckets[first].length
    ))[0];

    mixed.push(buckets[category].shift());
    if (category === lastCategory) streak += 1;
    else {
      lastCategory = category;
      streak = 1;
    }
  }

  return mixed;
};

const remainingImages = allImagePaths.filter((image) => !featuredSet.has(image));

export const galleryItems = [
  ...featuredImages.map((image) => createGalleryItem(image, true)),
  ...mixByCategory(remainingImages).map((image) => createGalleryItem(image)),
];
